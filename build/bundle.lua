--[[
	build/bundle.lua  —  the bundler
	=============================================================================
	Run with a standalone Lua/Luau interpreter from the repo root:
	    lua build/bundle.lua          (or)   luau build/bundle.lua

	Reads manifest.modules (the canonical file list + order), wraps every src file as
	    __modules["name"] = (function() <verbatim file source> end)()
	(the IIFE returns the file's `return function(require) ... end` factory), inlines
	a require-shim, and ends with `return __require("init")`. The result is written to
	dist/cinematic.lua — a single, offline, deterministic fat-file that boots the suite
	with no network. It also (re)writes dist/loader.lua's pinned constants from
	manifest.repo so the network loader stays in sync.

	This is pure Lua (io/string only) — no Roblox APIs — so CI can run it anywhere.
]]

local SRC_DIR = "src"
local DIST_DIR = "dist"

-- ── tiny fs helpers ──────────────────────────────────────────────────────────
local function readFile(path)
	local f = assert(io.open(path, "rb"), "cannot open " .. path)
	local data = f:read("*a")
	f:close()
	return data
end

local function writeFile(path, data)
	local f = assert(io.open(path, "wb"), "cannot write " .. path)
	f:write(data)
	f:close()
end

-- ── load the manifest table without a Roblox require ─────────────────────────
-- manifest.lua is `return function(require) return {...} end`.
local function loadManifest()
	local src = readFile(SRC_DIR .. "/manifest.lua")
	local factory = assert(loadstring or load)(src, "@manifest")()
	return factory(function() end)
end

local manifest = loadManifest()
assert(type(manifest.modules) == "table", "manifest.modules missing")

-- ── build the bundle ─────────────────────────────────────────────────────────
local out = {}
out[#out + 1] = "--[[ Cinematic Graphics Suite v" .. tostring(manifest.version)
	.. " — single-file bundle generated by build/bundle.lua. Do not edit by hand. ]]"
out[#out + 1] = [[
local __modules = {}
local __cache = {}
local __running = {}
local function __require(name)
	if __cache[name] ~= nil then return __cache[name] end
	local factory = __modules[name]
	if not factory then error("module not found: " .. tostring(name)) end
	if __running[name] then error("cyclic require: " .. tostring(name)) end
	__running[name] = true
	local result = factory(__require)
	__running[name] = nil
	__cache[name] = result
	return result
end
]]

for _, name in ipairs(manifest.modules) do
	local path = SRC_DIR .. "/" .. name .. ".lua"
	local body = readFile(path)
	-- Wrap so the file's top-level `return function(require) ... end` becomes the
	-- factory stored in __modules[name].
	out[#out + 1] = ("__modules[%q] = (function()\n%s\nend)()"):format(name, body)
end

out[#out + 1] = "return __require(\"init\")\n"

writeFile(DIST_DIR .. "/cinematic.lua", table.concat(out, "\n\n"))
print("Wrote " .. DIST_DIR .. "/cinematic.lua (" .. #manifest.modules .. " modules)")

-- ── keep dist/loader.lua's pinned constants in sync with manifest.repo ────────
local repo = manifest.repo or {}
local loaderPath = DIST_DIR .. "/loader.lua"
local ok, loaderSrc = pcall(readFile, loaderPath)
if ok then
	loaderSrc = loaderSrc:gsub('local USER = "[^"]*"', ('local USER = %q'):format(repo.user or "YOUR_GH_USERNAME"))
	loaderSrc = loaderSrc:gsub('local REPO = "[^"]*"', ('local REPO = %q'):format(repo.name or "cinematic-shader"))
	loaderSrc = loaderSrc:gsub('local REF  = "[^"]*"', ('local REF  = %q'):format(repo.ref or "v1.0.0"))
	loaderSrc = loaderSrc:gsub('local VERSION = "[^"]*"', ('local VERSION = %q'):format(manifest.version or "1.0.0"))
	writeFile(loaderPath, loaderSrc)
	print("Synced " .. loaderPath .. " constants from manifest.repo")
end

print("Bundle complete.")
